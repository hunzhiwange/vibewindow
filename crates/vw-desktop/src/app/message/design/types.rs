#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum VariableKindPreset {
    Color,
    Number,
    String,
}

impl VariableKindPreset {
    pub fn as_kind(self) -> &'static str {
        match self {
            Self::Color => "color",
            Self::Number => "number",
            Self::String => "string",
        }
    }

    pub fn label(self) -> &'static str {
        match self {
            Self::Color => "Color",
            Self::Number => "Number",
            Self::String => "String",
        }
    }

    pub fn default_name_prefix(self) -> &'static str {
        match self {
            Self::Color => "--color",
            Self::Number => "--number",
            Self::String => "--string",
        }
    }

    pub fn default_value(self) -> &'static str {
        match self {
            Self::Color => "#D4D4D8",
            Self::Number => "0",
            Self::String => "value",
        }
    }
}

