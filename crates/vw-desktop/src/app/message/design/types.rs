#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum LayerAction {
    ToggleVisible,
    MoveUp,
    MoveDown,
    Delete,
}

impl std::fmt::Display for LayerAction {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            LayerAction::ToggleVisible => write!(f, "👁"),
            LayerAction::MoveUp => write!(f, "↑"),
            LayerAction::MoveDown => write!(f, "↓"),
            LayerAction::Delete => write!(f, "🗑"),
        }
    }
}

