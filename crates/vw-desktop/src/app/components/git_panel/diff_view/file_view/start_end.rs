//! 差异视图间隙范围模块
//!
//! 本模块提供用于表示Git差异视图中折叠间隙范围的数据结构。
//! 在显示大型差异文件时，为提高性能和可读性，连续未更改的行
//! 可以被折叠成间隙，该结构用于记录这些间隙的位置信息。

/// 差异视图中的间隙范围表示
///
/// 该结构体用于记录在差异视图中被折叠的代码块的位置信息。
/// 当文件较大且包含大量未更改的行时，系统会将这些行折叠为间隙，
/// 只显示有实际改动的代码部分，从而提高界面性能和用户体验。
///
/// # 字段说明
///
/// * `start_old` - 旧文件（修改前）中的间隙起始行号
/// * `end_old` - 旧文件（修改前）中的间隙结束行号
/// * `start_new` - 新文件（修改后）中的间隙起始行号
/// * `gap_id` - 间隙的唯一标识符，用于在界面中追踪和管理间隙
///
/// # 示例
///
/// ```ignore
/// let gap = GapRange {
///     start_old: 10,
///     end_old: 50,
///     start_new: 10,
///     gap_id: 0,
/// };
/// // 表示旧文件的第10-50行和新文件的第10行开始的位置存在一个间隙
/// ```
#[derive(Clone, Copy)]
pub struct GapRange {
    /// 旧文件中的间隙起始行号（0-based索引）
    pub start_old: usize,

    /// 旧文件中的间隙结束行号（0-based索引）
    pub end_old: usize,

    /// 新文件中的间隙起始行号（0-based索引）
    pub start_new: usize,

    /// 间隙的唯一标识符，用于UI组件追踪和渲染
    pub gap_id: usize,
}
