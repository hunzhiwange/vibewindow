//! # Agent 模块
//!
//! 本模块是 VibeWindow 代理系统的核心入口点，负责组织和导出所有代理相关的子模块。
//!
//! ## 模块架构
//!
//! Agent 模块采用 trait 驱动的模块化架构，各个子系统通过定义清晰的 trait 接口进行解耦。
//! 主要子系统包括：
//!
//! - **核心编排**：`agent` - 代理的核心执行循环与调度逻辑
//! - **模型提供者**：`provider`、`providers` - LLM 模型适配与调用
//! - **通道集成**：`channels` - 多平台消息通道（Telegram/Slack/Discord 等）
//! - **工具执行**：`tools` - Shell/文件/进程等工具执行面
//! - **记忆存储**：`memory` - 多后端记忆层（Markdown/SQLite/PostgreSQL/Vector）
//! - **安全防护**：`security` - 策略、隔离、泄漏检测
//! - **运行时适配**：`runtime` - Native/Docker/WASM 运行时
//! - **观测性**：`observability` - 日志与观测后端聚合
//!
//! ## 扩展方式
//!
//! 大多数扩展工作应通过实现对应的 trait 并在工厂模块中注册完成：
//!
//! - `Provider` trait → 在 `provider/` 中实现
//! - `Channel` trait → 在 `channels/` 中实现
//! - `Tool` trait → 在 `tools/` 中实现
//! - `Memory` trait → 在 `memory/` 中实现
//!
//! ## 平台兼容性
//!
//! 部分模块仅在非 WASM 目标平台上可用，这些模块使用 `#[cfg(not(target_arch = "wasm32"))]`
//! 进行条件编译标记。

