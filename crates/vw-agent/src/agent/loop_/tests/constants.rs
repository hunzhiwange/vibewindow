/// 编译期常量边界检查
///
/// 该常量块在编译时验证所有关键常量值是否满足以下约束：
///
/// # 约束条件
///
/// | 常量名 | 下界 | 上界 | 说明 |
/// |--------|------|------|------|
/// | `DEFAULT_MAX_TOOL_ITERATIONS` | 1 | 100 | 确保工具迭代次数既不会为零（无法执行），也不会过大（避免无限循环） |
/// | `DEFAULT_MAX_HISTORY_MESSAGES` | 1 | 1000 | 确保历史消息数量合理，避免内存过度消耗 |
///
/// # 失败行为
///
/// 如果任一断言失败，编译将无法通过，并显示相应的断言错误。
///
/// # 示例
///
/// ```compile_fail
/// // 如果 DEFAULT_MAX_TOOL_ITERATIONS 设置为 0，编译将失败
/// const _: () = { assert!(0 > 0); }; // 编译错误！
/// ```
const _: () = {
    // 验证工具迭代最大次数在有效范围内
    // 下界检查：必须大于0，确保至少可以执行一次工具调用
    assert!(DEFAULT_MAX_TOOL_ITERATIONS > 0);
    // 上界检查：不超过100次，防止过长的执行链导致资源耗尽
    assert!(DEFAULT_MAX_TOOL_ITERATIONS <= 100);

    // 验证历史消息最大数量在有效范围内
    // 下界检查：必须大于0，确保至少保留一条消息用于上下文
    assert!(DEFAULT_MAX_HISTORY_MESSAGES > 0);
    // 上界检查：不超过1000条，平衡内存使用与上下文完整性
    assert!(DEFAULT_MAX_HISTORY_MESSAGES <= 1000);
};

