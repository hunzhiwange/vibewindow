//! # 常量验证测试模块
//!
//! 本模块负责在编译期验证代理循环（agent loop）中使用的核心常量值是否符合预期边界约束。
//!
//! ## 主要功能
//!
//! - **编译期断言验证**：通过常量断言确保关键配置值在合理范围内
//! - **安全性保障**：防止配置常量被意外设置为无效或危险值
//!
//! ## 验证的常量
//!
//! - `DEFAULT_MAX_TOOL_ITERATIONS`：工具调用最大迭代次数
//! - `DEFAULT_MAX_HISTORY_MESSAGES`：历史消息最大保留数量
//!
//! ## 设计原理
//!
//! 使用编译期常量断言（const assertions）而非运行时检查的优势：
//! 1. **零运行时开销**：验证在编译阶段完成
//! 2. **提前发现问题**：无效配置无法通过编译
//! 3. **文档化约束**：清晰表达常量的合法取值范围

