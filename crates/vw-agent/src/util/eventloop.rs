//! 事件循环工具模块
//!
//! 本模块提供事件循环相关的辅助工具函数，用于控制异步任务的执行调度。
//!
//! # 主要功能
//!
//! - 提供任务让出（yield）机制，允许其他任务在事件循环中获得执行机会
//! - 支持跨平台运行，包括原生平台和 WebAssembly 平台

/// 让出当前任务的执行权
///
/// 此函数用于在异步事件循环中主动让出当前任务的执行权，
/// 允许事件循环调度器执行其他待处理的任务。
///
/// # 平台差异
///
/// - **非 WebAssembly 平台**：调用 Tokio 的 `yield_now()` 函数，
///   立即让出执行权并等待调度器重新调度
/// - **WebAssembly 平台**：函数体为空（无操作），
///   因为 WASM 运行时的异步模型与原生平台不同
///
/// # 使用场景
///
/// - 在长时间运行的循环中防止独占 CPU 时间片
/// - 确保其他异步任务有机会执行
/// - 在计算密集型任务中实现协作式多任务调度
///
/// # 示例
///
/// ```rust,no_run
/// use app::agent::util::eventloop::wait;
///
/// async fn long_running_task() {
///     loop {
///         // 执行一些工作
///         do_some_work();
///
///         // 让出执行权，允许其他任务运行
///         wait().await;
///     }
/// }
/// #
/// # fn do_some_work() {}
/// ```
pub async fn wait() {
    // 非 WebAssembly 架构：使用 Tokio 的 yield_now 让出执行权
    // 这会将当前任务放回调度器队列，允许其他任务执行
    #[cfg(not(target_arch = "wasm32"))]
    tokio::task::yield_now().await;

    // WebAssembly 架构：无需显式让出
    // WASM 运行时使用不同的异步模型，不需要手动让出
}
