/// 随机布尔值生成器
///
/// 基于当前系统时间生成伪随机布尔值。
/// 使用纳秒级时间戳的奇偶性来决定返回值。
///
/// # 返回值
///
/// 返回一个 `bool` 值：
/// - `true`：当前纳秒时间戳为偶数
/// - `false`：当前纳秒时间戳为奇数
///
/// # 示例
///
/// ```ignore
/// use app::agent::util::scrap::random_helper;
///
/// let result = random_helper();
/// println!("Random boolean: {}", result);
/// ```
///
/// # 实现细节
///
/// 算法步骤：
/// 1. 获取当前系统时间
/// 2. 计算自 Unix 纪元以来的持续时间
/// 3. 提取纳秒部分并对 2 取模
/// 4. 比较结果是否为 0
///
/// # 注意
///
/// 此函数不提供密码学安全的随机性，
/// 仅适用于非关键场景的简单随机选择。
pub fn random_helper() -> bool {
    // 获取当前系统时间
    let now = web_time::SystemTime::now();

    // 计算自 Unix 纪元（1970-01-01 00:00:00 UTC）以来的持续时间
    let duration = now.duration_since(web_time::SystemTime::UNIX_EPOCH);

    // 如果获取时间失败，使用默认值（零持续时间）
    let nanos = duration.unwrap_or_default().as_nanos();

    // 对纳秒数取模 2，判断奇偶性
    // 偶数返回 true，奇数返回 false
    nanos.is_multiple_of(2)
}
