//! 快照模块的单元测试
//!
//! 本模块提供对 memory/snapshot 功能的完整测试覆盖，包括：
//! - 快照文件解析（从 Markdown 格式提取记忆条目）
//! - 快照导出（将核心记忆持久化为 Markdown 文件）
//! - 快照水合/恢复（从快照文件重建数据库）
//! - 边界条件与错误路径验证
//!
//! 测试策略：
//! - 使用 `tempfile` crate 创建隔离的临时工作区，确保测试之间互不干扰
//! - 采用端到端测试风格，验证从数据库到快照文件的完整往返
//! - 覆盖正常路径、空状态、多行内容、过滤非核心记忆等场景

use super::*;
use tempfile::TempDir;

/// 测试基础快照解析功能
///
/// 验证点：
/// - 能够正确解析包含 2 个核心记忆的标准 Markdown 快照格式
/// - 提取的键（key）与预期一致（identity, preference_lang）
/// - 提取的内容包含预期关键词（self-preserving, Rust）
/// - 解析器能跳过元数据部分（导出时间、总数等）
#[test]
fn parse_snapshot_basic() {
    let input = r#"# 💡 VibeWindow Memory Snapshot

> Auto-generated by VibeWindow.

**Last exported:** 2025-01-15 14:30:00

**Total core memories:** 2

---

### 🔑 `identity`

I am VibeWindow, a self-preserving AI agent.

*Created: 2025-01-15 | Updated: 2025-01-15*

---

### 🔑 `preference_lang`

The user prefers Rust for systems programming.

*Created: 2025-01-14 | Updated: 2025-01-15*

---
"#;

    let entries = parse_snapshot(input);
    assert_eq!(entries.len(), 2);
    assert_eq!(entries[0].0, "identity");
    assert!(entries[0].1.contains("self-preserving"));
    assert_eq!(entries[1].0, "preference_lang");
    assert!(entries[1].1.contains("Rust"));
}

/// 测试空快照的解析
///
/// 验证点：
/// - 当快照文件只包含元数据头部而没有记忆条目时，应返回空列表
