/// 测试完整的导出-水合往返流程
///
/// 这是快照功能的核心端到端测试，模拟完整的灾难恢复场景：
/// 1. 创建包含核心记忆的数据库
/// 2. 导出快照文件
/// 3. 模拟数据库丢失（删除 brain.db）
/// 4. 从快照文件恢复数据
///
/// 验证点：
/// - 只有核心记忆（category='core'）被导出，非核心记忆被过滤
/// - 导出的快照文件包含预期的 Markdown 格式和内容
/// - should_hydrate 能正确检测需要恢复的场景
/// - 水合操作能完整恢复所有核心记忆
/// - 恢复后的数据库包含正确的数据结构和内容
#[test]
fn export_and_hydrate_roundtrip() {
    let tmp = TempDir::new().unwrap();
    let workspace = tmp.path();
    let storage = paths::project_data_dir(workspace).unwrap();

    // 手动创建包含核心记忆的 brain.db 数据库
    let db_dir = storage.join("memory");
    fs::create_dir_all(&db_dir).unwrap();
    let db_path = db_dir.join("brain.db");

    // 初始化数据库 schema 和 WAL 模式
    let conn = Connection::open(&db_path).unwrap();
    conn.execute_batch(
        "PRAGMA journal_mode = WAL;
         CREATE TABLE IF NOT EXISTS memories (
            id TEXT PRIMARY KEY,
            key TEXT NOT NULL UNIQUE,
            content TEXT NOT NULL,
            category TEXT NOT NULL DEFAULT 'core',
            embedding BLOB,
            created_at TEXT NOT NULL,
            updated_at TEXT NOT NULL
         );
         CREATE INDEX IF NOT EXISTS idx_mem_key ON memories(key);",
    )
    .unwrap();

    // 插入两条核心记忆用于测试导出
    let now = Local::now().to_rfc3339();
    conn.execute(
        "INSERT INTO memories (id, key, content, category, created_at, updated_at)
         VALUES ('id1', 'identity', 'I am a test agent', 'core', ?1, ?2)",
        params![now, now],
    )
    .unwrap();
    conn.execute(
        "INSERT INTO memories (id, key, content, category, created_at, updated_at)
         VALUES ('id2', 'preference', 'User likes Rust', 'core', ?1, ?2)",
        params![now, now],
    )
    .unwrap();
    // 插入一条非核心记忆（对话类型），验证其不会被导出
    conn.execute(
        "INSERT INTO memories (id, key, content, category, created_at, updated_at)
         VALUES ('id3', 'conv1', 'Random convo', 'conversation', ?1, ?2)",
        params![now, now],
    )
    .unwrap();
    drop(conn);

    // 执行快照导出
    let exported = export_snapshot(workspace).unwrap();
    assert_eq!(exported, 2, "应仅导出核心记忆");

    // 验证快照文件已创建且内容正确
    let snapshot = storage.join(SNAPSHOT_FILENAME);
    assert!(snapshot.exists());
    let content = fs::read_to_string(&snapshot).unwrap();
    assert!(content.contains("identity"));
    assert!(content.contains("I am a test agent"));
    assert!(content.contains("preference"));
    assert!(!content.contains("Random convo"));

    // 模拟灾难性故障：删除 brain.db 文件
    fs::remove_file(&db_path).unwrap();
    assert!(!db_path.exists());

    // 验证 should_hydrate 能检测到需要恢复的场景
    assert!(should_hydrate(workspace));

    // 从快照文件执行水合恢复
    let hydrated = hydrate_from_snapshot(workspace).unwrap();
    assert_eq!(hydrated, 2, "应恢复两条核心记忆");

    // 验证 brain.db 已被重新创建
    assert!(db_path.exists());

    // 验证恢复后的数据库包含正确的数据
    let conn = Connection::open(&db_path).unwrap();
    let count: i64 = conn.query_row("SELECT COUNT(*) FROM memories", [], |row| row.get(0)).unwrap();
    assert_eq!(count, 2);

    // 验证具体的记忆内容
    let identity: String = conn
        .query_row("SELECT content FROM memories WHERE key = 'identity'", [], |row| row.get(0))
        .unwrap();
    assert_eq!(identity, "I am a test agent");
}

/// 测试 should_hydrate 函数的条件判断逻辑
///
/// should_hydrate 应仅在以下条件同时满足时返回 true：
/// - 快照文件存在
/// - 数据库文件不存在（需要恢复）
///
/// 验证场景：
/// 1. 无数据库、无快照 → false（无需恢复）
/// 2. 有快照、无数据库 → true（需要恢复）
/// 3. 有数据库、有快照 → false（数据库已存在，无需恢复）
#[test]
fn should_hydrate_only_when_needed() {
    let tmp = TempDir::new().unwrap();
    let workspace = tmp.path();
    let storage = paths::project_data_dir(workspace).unwrap();

    // 场景 1：无数据库、无快照 → 应返回 false
    assert!(!should_hydrate(workspace));

    // 场景 2：创建快照但没有数据库 → 应返回 true
    let snapshot = storage.join(SNAPSHOT_FILENAME);
    fs::create_dir_all(&storage).unwrap();
    fs::write(&snapshot, "### 🔑 `test`\n\nHello\n").unwrap();
    assert!(should_hydrate(workspace));

    // 场景 3：创建真实数据库 → 应返回 false（即使快照存在）
    let db_dir = storage.join("memory");
    fs::create_dir_all(&db_dir).unwrap();
    let db_path = db_dir.join("brain.db");
    let conn = Connection::open(&db_path).unwrap();
    conn.execute_batch(
        "CREATE TABLE IF NOT EXISTS memories (
            id TEXT PRIMARY KEY,
            key TEXT NOT NULL UNIQUE,
            content TEXT NOT NULL,
            category TEXT NOT NULL DEFAULT 'core',
            embedding BLOB,
            created_at TEXT NOT NULL,
            updated_at TEXT NOT NULL
         );
         INSERT INTO memories VALUES('x','x','x','core',NULL,'2025-01-01','2025-01-01');",
    )
    .unwrap();
    drop(conn);
    assert!(!should_hydrate(workspace));
}

