//! 权限管理模块
//!
//! 本模块提供了代理系统的权限控制机制，用于管理和验证工具调用、
//! 文件访问、命令执行等操作的权限。
//!
//! # 子模块
//!
//! - [`arity`] - 命令元数（参数数量）定义模块，维护各类工具命令的参数数量映射
//! - [`next`] - 权限请求与响应处理模块，提供权限规则评估、请求管理和用户交互功能
//!
//! # 主要功能
//!
//! - 命令参数数量校验：确保工具调用时参数数量符合预期
//! - 权限规则评估：基于配置规则判断操作的允许/拒绝/询问状态
//! - 请求队列管理：管理待处理的权限请求
//! - 用户交互支持：提供权限请求的批准、拒绝、永久允许等响应机制

pub mod arity;
pub mod next;
