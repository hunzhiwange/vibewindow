//! # 技能模块（Skill Module）
//!
//! 本模块是 VibeWindow 代理系统的技能生态核心入口，负责技能的发现、加载、审计、安装与执行。
//!
//! ## 模块职责
//!
//! - **技能发现与加载**：从文件系统、远程仓库或内置源发现并加载可用技能
//! - **技能安装与管理**：提供技能的安装、卸载、更新等生命周期管理功能
//! - **技能审计与安全**：对技能进行安全审计，验证来源与权限策略
//! - **技能提示注入**：将技能信息转换为可注入代理提示的格式
//! - **技能执行策略**：定义技能下载、运行时的安全策略与配置
//!
//! ## 子模块结构
//!
//! | 模块 | 可见性 | 职责 |
//! |------|--------|------|
//! | `audit` | 公开 | 技能安全审计与验证 |
//! | `discovery` | 内部 | 技能发现与扫描 |
//! | `skill` | 内部 | 技能核心定义与执行逻辑 |
//! | `constants` | 内部 | 技能相关常量定义 |
//! | `install` | 内部 | 技能安装命令处理 |
//! | `loader` | 内部 | 技能加载器实现 |
//! | `open_skills` | 内部 | Open Skills 协议支持 |
//! | `policy` | 内部 | 技能安全策略与域名信任验证 |
//! | `prompt` | 内部 | 技能到提示的转换逻辑 |
//! | `source` | 内部 | 技能来源（本地/远程）管理 |
//! | `types` | 内部 | 技能相关类型定义 |
//!
//! ## 使用示例
//!
//! ```rust,ignore
//! use crate::app::agent::skill::{load_skills, skills_to_prompt, Skill};
//!
//! // 加载技能
//! let skills = load_skills("/path/to/skills")?;
//!
//! // 将技能转换为提示文本
//! let prompt = skills_to_prompt(&skills);
//! ```
//!
//! ## 架构约定
//!
//! - 公开 API 通过 `pub use` 重新导出，隐藏内部模块结构
//! - 内部实现细节封装在各子模块中，通过 `mod` 声明
//! - 仅导出 crate 内部使用的符号使用 `pub(crate)` 可见性
//!
//! ## 安全注意事项
//!
//! 技能模块涉及从外部源加载和执行代码，需要特别关注：
//! - 来源验证与域名信任策略（见 `policy` 模块）
//! - 下载策略与安全审计（见 `audit` 模块）
//! - 运行时权限限制（见 `types::SkillRuntimeConfig`）

// ============================================================================
// 公开子模块
// ============================================================================

/// 技能安全审计模块
///
/// 提供技能的安全审计功能，包括来源验证、权限检查、恶意代码检测等。
/// 此模块对外公开，允许外部调用者对技能进行独立审计。
pub mod audit;

// ============================================================================
// 内部子模块
// ============================================================================

/// 技能发现模块
///
/// 负责从文件系统、远程仓库等源扫描和发现可用技能。
/// 支持多种发现策略，包括目录扫描、Git 仓库克隆等。
mod discovery;

/// 技能核心模块
///
/// 包含技能的核心定义、执行逻辑和生命周期管理。
/// 使用 `*` 通配符重新导出，简化外部使用。
mod skill;

/// 技能常量模块
///
/// 定义技能系统使用的各种常量，如默认目录名、文件扩展名、
/// 信任域名列表、超时配置等。
mod constants;

/// 技能安装模块
///
/// 处理技能的安装命令，包括从远程源下载、解压、验证和部署。
/// 同时提供技能目录的初始化功能。
mod install;

/// 技能加载器模块
///
/// 提供技能的加载功能，从配置或目录中读取技能定义并实例化。
/// 支持带配置加载和无配置加载两种模式。
mod loader;

/// Open Skills 协议模块
///
/// 实现对 Open Skills 协议的支持，允许代理与标准化的技能生态系统互操作。
/// 提供协议版本的解析、验证和兼容性处理。
mod open_skills;

/// 技能安全策略模块
///
/// 定义和实现技能的安全策略，包括：
/// - 下载策略（允许/禁止/询问）
/// - 域名信任验证
/// - 来源别名解析
mod policy;

/// 技能提示注入模块
///
/// 将技能定义转换为可注入代理提示的文本格式。
/// 支持多种注入模式，以适应不同的代理架构和提示格式要求。
mod prompt;

/// 技能来源管理模块
///
/// 管理技能的各种来源类型，包括本地文件路径、Git 仓库 URL、
/// 内置技能等。提供来源的解析、验证和访问功能。
mod source;

/// 技能类型定义模块
///
/// 定义技能系统使用的核心类型，包括：
/// - `Skill`: 技能定义
/// - `SkillTool`: 技能工具
/// - `SkillsConfig`: 技能配置
/// - `SkillRuntimeConfig`: 运行时配置
/// - `SkillDownloadPolicy`: 下载策略
mod types;

// ============================================================================
// 公开接口重新导出
// ============================================================================

/// 技能安装模块的公开接口
///
/// 重新导出技能安装相关的命令处理函数和目录初始化函数。
pub use install::{handle_command, init_skills_dir};

/// 技能加载模块的公开接口
///
/// 重新导出技能加载函数，支持从目录或配置加载技能列表。
pub use loader::{load_skills, load_skills_with_config};

/// 技能提示模块的公开接口
///
/// 重新导出技能目录访问和技能到提示转换的函数。
pub use prompt::{skills_dir, skills_to_prompt, skills_to_prompt_with_mode};

/// 技能核心模块的公开接口
///
/// 重新导出 `skill` 模块中的所有公开类型和函数。
/// 包括技能执行器、运行时处理等核心功能。
pub use skill::*;

/// 技能类型模块的公开接口
///
/// 重新导出技能系统的核心类型定义，供外部模块使用。
///
/// # 导出类型
///
/// - `Skill`: 技能的完整定义，包含名称、描述、工具列表等
/// - `SkillCommands`: 技能支持的命令集合
/// - `SkillRuntimeConfig`: 技能的运行时配置，如超时、资源限制等
/// - `SkillTool`: 技能提供的工具定义
/// - `SkillsConfig`: 技能集合的全局配置
/// - `SkillsPromptInjectionMode`: 技能信息注入提示的模式选择
pub use types::{
    Skill, SkillCommands, SkillRuntimeConfig, SkillTool, SkillsConfig, SkillsPromptInjectionMode,
};

