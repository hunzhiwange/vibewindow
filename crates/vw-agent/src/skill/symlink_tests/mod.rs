//! 符号链接测试模块
//!
//! 本模块负责组织和导出符号链接相关的测试用例。通过条件编译，仅在测试构建时包含，
//! 用于验证技能目录中符号链接的创建、解析和安全性等功能。
//!
//! # 模块结构
//!
//! - 使用 `#[cfg(test)]` 属性确保测试代码仅在测试配置下编译
//! - 使用 `#[path]` 属性显式指定测试实现的源文件路径
//! - 引入 `tests.rs` 作为实际的测试实现模块

/// 测试条件编译属性
///
/// 该属性确保本模块及其依赖的测试代码仅在执行 `cargo test` 或类似测试命令时编译，
/// 不会包含在正式构建产物中，从而避免增加二进制体积和引入不必要的依赖。
#[cfg(test)]
/// 测试模块路径属性
///
/// 显式指定测试模块的实现文件为同级目录下的 `tests.rs`。
/// 这允许测试代码与模块声明分离，保持代码组织清晰。
#[path = "tests.rs"]
