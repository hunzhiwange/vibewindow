//! # 心跳模块 (Heartbeat Module)
//!
//! 本模块提供 Agent 实例的心跳检测与健康监控功能。
//!
//! ## 核心功能
//!
//! - **心跳引擎**：定期向监控系统发送心跳信号，表明 Agent 实例存活
//! - **健康检查**：监控 Agent 运行状态，检测异常情况
//! - **状态报告**：收集并上报 Agent 的关键指标和运行时信息
//!
//! ## 模块结构
//!
//! - [`engine`]：心跳引擎实现，包含心跳发送、超时检测、重试机制等核心逻辑
//!
//! ## 使用场景
//!
//! 1. **分布式部署**：在多节点部署场景下，用于检测各节点存活状态
//! 2. **故障恢复**：当心跳超时时，触发告警或自动恢复机制
//! 3. **负载均衡**：心跳信息可用于服务发现和负载均衡决策
//!
//! ## 示例
//!
//! ```ignore
//! use vibe_agent::heartbeat::engine::HeartbeatEngine;
//!
//! // 创建并启动心跳引擎
//! let engine = HeartbeatEngine::new(config);
//! engine.start().await?;
//! ```

